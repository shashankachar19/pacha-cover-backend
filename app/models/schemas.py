# FEATURE 4 — Bhasha: Vernacular AI Voice Interface
# ══════════════════════════════════════════════════════════════════════════════

class SupportedLanguage(str, Enum):
    """Languages supported by the Bhasha voice interface."""
    KANNADA = "kn"         # Primary — Bengaluru's official language
    HINDI = "hi"
    TAMIL = "ta"
    TELUGU = "te"
    ENGLISH = "en"


class TranscriptionResult(BaseModel):
    """Output from Google Cloud Speech-to-Text."""
    transcript_english: str = Field(
        ..., description="Transcript converted to English for Gemini"
    )
    transcript_original: str = Field(
        ..., description="Transcript in the source language"
    )
    source_language: SupportedLanguage
    confidence: float = Field(..., ge=0, le=1)
    # BCP-47 language code used by Speech-to-Text
    detected_language_code: str


class VoicePrescriptionResponse(BaseModel):
    """
    Full response for POST /voice/prescribe.
    Contains the prescription PLUS Kannada (or source-language) translations
    of every human-readable field so the frontend can drive TTS.
    """
    # ── What the user said ───────────────────────────────────────────────────
    transcription: TranscriptionResult

    # ── English prescription (same shape as /prescribe) ─────────────────────
    prescription: PrescriptionResponse

    # ── Vernacular translation of the prescription ───────────────────────────
    # Key fields translated back for voice readback
    primary_species_name_vernacular: str = Field(
        ...,
        description="Common name in the user's language (e.g. Kannada: ಬೇವು)",
    )
    why_recommended_vernacular: str = Field(
        ...,
        description="why_recommended translated back to source language",
    )
    full_prescription_vernacular: str = Field(
        ...,
        description="Complete prescription summary in the source language, "
                    "ready for Text-to-Speech playback",
    )

    # ── Audio response (optional — set if TTS is enabled) ───────────────────
    # Base64-encoded MP3 bytes of the vernacular prescription readback
    audio_response_base64: str | None = None
    audio_content_type: str = "audio/mp3"

    # ── Metadata ─────────────────────────────────────────────────────────────
    source_language: SupportedLanguage
    processing_steps: list[str] = Field(
        default_factory=list,
        description="Ordered list of pipeline steps for debugging / transparency",
    )
