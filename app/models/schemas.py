# FEATURE 3 — AI Carbon Credit & Tax Simulator
# ══════════════════════════════════════════════════════════════════════════════

class CarbonCreditRequest(BaseModel):
    """
    Input for POST /carbon/simulate.
    Can be called with a known species or raw coordinates
    (in which case we use the recommended species' CO2 data).
    """
    species_common_name: str = Field(..., examples=["Neem"])
    species_scientific_name: str | None = Field(
        None, examples=["Azadirachta indica"]
    )
    tree_age_years: float = Field(
        ..., gt=0, le=200,
        description="Age of the tree / expected age at evaluation",
        examples=[5.0],
    )
    num_trees: int = Field(
        default=1, ge=1, le=10000,
        description="Number of trees of this species to simulate",
    )
    # Optional: property value for rebate calculation
    property_value_inr: float | None = Field(
        None, gt=0,
        description="Current property valuation in INR for tax rebate simulation",
        examples=[5000000.0],
    )


class AnnualCarbonProfile(BaseModel):
    """Gemini-computed carbon sequestration breakdown for one year."""
    year: int
    co2_kg_sequestered: float
    cumulative_co2_kg: float
    # Equivalent in common units for public communication
    equivalent_car_km_offset: float = Field(
        ..., description="km a car would travel emitting this much CO2"
    )
    equivalent_flights_offset: float = Field(
        ..., description="Short-haul flights (~1000km) this offsets"
    )


class TaxRebateSimulation(BaseModel):
    """
    Simulated BBMP property tax rebate based on CO2 sequestration.
    Rule: 1% rebate for every 20 kg CO2 sequestered per year, capped at 20%.
    """
    annual_co2_kg: float
    rebate_percent: float = Field(..., ge=0, le=20)
    rebate_amount_inr: float | None = None  # None if property_value not supplied
    # Explanation for citizens
    rebate_calculation_note: str


class CarbonCreditResponse(BaseModel):
    """Full simulation result for POST /carbon/simulate"""
    species_common_name: str
    species_scientific_name: str | None
    num_trees: int
    tree_age_years: float

    # ── Carbon sequestration ─────────────────────────────────────────────────
    annual_co2_kg_per_tree: float
    total_annual_co2_kg: float
    cumulative_co2_kg_lifetime: float   # Integrated over tree_age_years

    # Year-by-year profile (first 10 years or up to tree_age, whichever shorter)
    annual_profile: list[AnnualCarbonProfile] = []

    # ── Carbon credit market value ───────────────────────────────────────────
    # Using India's voluntary carbon market rate (~₹800–1200 per tonne CO2)
    carbon_credit_value_inr: float
    carbon_credit_rate_inr_per_tonne: float = 1000.0

    # ── Tax rebate ───────────────────────────────────────────────────────────
    tax_rebate: TaxRebateSimulation

    # ── Gemini narrative ─────────────────────────────────────────────────────
    gemini_narrative: str = Field(
        ...,
        description="Plain-language Gemini explanation of the environmental ROI",
    )
    gemini_model_used: str
    disclaimer: str = (
        "Carbon estimates are AI-generated approximations. "
        "Tax rebates are illustrative simulations — not official BBMP policy. "
        "Consult a certified carbon auditor for compliance purposes."
    )


