# ============================================================
# app/models/schemas.py
#
# Pydantic v2 models used across the entire API.
# These are the "contracts" between our backend and all clients
# (mobile app, web dashboard, other microservices).
#
# Naming convention:
#   <Entity>Create  — payload for POST (creating a resource)
#   <Entity>Update  — payload for PATCH (partial update)
#   <Entity>Out     — response sent to the client
#
# This file consolidates models from the original Pacha Cover
# codebase and all four extended features:
#   Feature 1: Pacha Vision AR
#   Feature 2: Green Corridor Clustering
#   Feature 3: AI Carbon Credit & Tax Simulator
#   Feature 4: Bhasha Vernacular Voice Interface
# ============================================================

from __future__ import annotations

from datetime import datetime
from enum import Enum
from typing import Any

from pydantic import BaseModel, Field, field_validator


# ── Shared primitives ──────────────────────────────────────────────────────────

class Coordinates(BaseModel):
    """Geographic point. Latitude/longitude in WGS-84."""
    latitude: float = Field(..., ge=-90, le=90, examples=[12.9716])
    longitude: float = Field(..., ge=-180, le=180, examples=[77.5946])


class APIResponse(BaseModel):
    """Generic wrapper for successful responses."""
    success: bool = True
    message: str = "OK"
    data: Any = None


# ── Heat Map ───────────────────────────────────────────────────────────────────

class HeatRiskLevel(str, Enum):
    LOW = "low"
    MODERATE = "moderate"
    HIGH = "high"
    CRITICAL = "critical"


class WardHeatData(BaseModel):
    """
    Heat and greenery snapshot for one BBMP ward.
    Populated from Google Earth Engine (NDVI + LST bands).
    """
    ward_id: str = Field(..., examples=["ward_147"])
    ward_name: str = Field(..., examples=["Koramangala"])
    # Land Surface Temperature in °C (from Landsat/MODIS LST band)
    avg_land_surface_temp: float = Field(..., description="°C")
    # Normalised Difference Vegetation Index (-1 to 1)
    avg_ndvi: float = Field(..., ge=-1, le=1)
    # Percentage of ward area classified as green cover
    green_cover_percent: float = Field(..., ge=0, le=100)
    # Composite risk score (0–100)
    heat_risk_score: float = Field(..., ge=0, le=100)
    heat_risk_level: HeatRiskLevel
    # Number of adopted spots in this ward (from Firestore)
    adopted_spots_count: int = 0
    last_updated: datetime = Field(default_factory=datetime.utcnow)
    # GeoJSON polygon of the ward boundary (optional for map rendering)
    boundary_geojson: dict | None = None


class HeatMapResponse(BaseModel):
    """Response for GET /heatmap"""
    wards: list[WardHeatData]
    data_source: str = "Google Earth Engine (Landsat 8 + MODIS)"
    generated_at: datetime = Field(default_factory=datetime.utcnow)
    total_wards: int


# ── Prescription / Species Recommender ────────────────────────────────────────

class PrescriptionRequest(BaseModel):
    """
    Input for POST /prescribe.
    The client sends GPS coords + optional context from on-device sensors.
    """
    coordinates: Coordinates
    # Optional enrichment — mobile app can include these for better recs
    nearby_land_use: str | None = Field(
        None,
        examples=["residential", "commercial", "roadside", "park"],
    )
    soil_type: str | None = Field(None, examples=["red laterite", "clay", "sandy"])
    plot_area_sqm: float | None = Field(None, gt=0, examples=[25.0])
    ward_name: str | None = Field(None, examples=["Koramangala"])


class TreeSpecies(BaseModel):
    """A recommended tree species with Gemini-generated rationale."""
    common_name: str = Field(..., examples=["Neem"])
    scientific_name: str = Field(..., examples=["Azadirachta indica"])
    kannada_name: str | None = Field(None, examples=["ಬೇವು"])
    why_recommended: str = Field(
        ...,
        description="Plain-language explanation generated by Gemini 1.5 Pro",
    )
    # Practical planting metadata
    expected_canopy_spread_m: float | None = None
    water_requirement: str | None = Field(None, examples=["Low", "Medium", "High"])
    growth_rate: str | None = Field(None, examples=["Fast", "Moderate", "Slow"])
    co2_absorption_kg_per_year: float | None = None
    # Rotary Focus Area alignment
    rotary_focus: str = Field(
        default="Environment",
        examples=["Environment", "Community Economic Development"],
    )


class PrescriptionResponse(BaseModel):
    """Response for POST /prescribe"""
    coordinates: Coordinates
    primary_recommendation: TreeSpecies
    alternative_recommendations: list[TreeSpecies] = []
    gemini_model_used: str
    disclaimer: str = (
        "Recommendations are AI-generated. Consult a local horticulturist "
        "for site-specific advice."
    )


# ── Green Ledger / Adopt-a-Spot ────────────────────────────────────────────────

class AdoptionStatus(str, Enum):
    PLEDGED = "pledged"          # User committed but hasn't planted yet
    PLANTED = "planted"          # Tree planted, awaiting first verification
    VERIFIED = "verified"        # At least one successful growth verification
    COMPLETED = "completed"      # Reached 3 verifications (full lifecycle)
    ABANDONED = "abandoned"      # Flagged inactive


class AdoptSpotCreate(BaseModel):
    """Payload for POST /ledger/adopt"""
    coordinates: Coordinates
    spot_name: str = Field(
        ..., min_length=3, max_length=100,
        examples=["Near HSR Layout Metro Station"],
    )
    ward_name: str = Field(..., examples=["Koramangala"])
    species_common_name: str = Field(..., examples=["Neem"])
    species_scientific_name: str | None = None
    notes: str | None = Field(None, max_length=500)
    # Whether the user wants this spot to be visible on the public map
    is_public: bool = True

    @field_validator("spot_name")
    @classmethod
    def strip_whitespace(cls, v: str) -> str:
        return v.strip()


class AdoptSpotOut(BaseModel):
    """A single adopted spot — returned from Firestore."""
    spot_id: str
    user_id: str
    coordinates: Coordinates
    spot_name: str
    ward_name: str
    species_common_name: str
    species_scientific_name: str | None
    notes: str | None
    status: AdoptionStatus
    is_public: bool
    green_points_earned: int = 0
    verification_count: int = 0
    adopted_at: datetime
    last_updated: datetime

    class Config:
        use_enum_values = True


class AdoptSpotUpdate(BaseModel):
    """Payload for PATCH /ledger/{spot_id} — all fields optional."""
    spot_name: str | None = None
    notes: str | None = None
    status: AdoptionStatus | None = None
    is_public: bool | None = None


# ── Verification Pipeline ──────────────────────────────────────────────────────

class VerificationStatus(str, Enum):
    APPROVED = "approved"    # Vertex AI detected a healthy sapling
    REJECTED = "rejected"    # No valid sapling detected
    PENDING = "pending"      # Queued for async processing


class VerificationResult(BaseModel):
    """
    Result of sending an image to Vertex AI for sapling detection.
    """
    spot_id: str
    verification_id: str
    status: VerificationStatus
    confidence_score: float = Field(..., ge=0, le=1)
    # Vertex AI model output labels
    detected_labels: list[str] = []
    # Points awarded (0 if rejected)
    green_points_awarded: int = 0
    message: str
    verified_at: datetime = Field(default_factory=datetime.utcnow)
    # GCS URI of the uploaded image
    image_gcs_uri: str | None = None


# ── User Profile ───────────────────────────────────────────────────────────────

class UserProfileOut(BaseModel):
    """Public-facing user profile with Green Ledger stats."""
    user_id: str
    display_name: str | None
    email: str | None
    total_green_points: int = 0
    total_trees_adopted: int = 0
    total_trees_verified: int = 0
    # City-wide rank (computed periodically by a Cloud Function)
    city_rank: int | None = None
    joined_at: datetime
    # Badge system — earned milestones
    badges: list[str] = []


