# FEATURE 1 — Pacha Vision: AR Metadata
# ══════════════════════════════════════════════════════════════════════════════

class ARModelScale(BaseModel):
    """
    Real-world scale hints for placing a 3D tree model in AR space.
    The frontend multiplies these by a scene unit factor.
    """
    x: float = Field(..., gt=0, description="Width scale (metres)")
    y: float = Field(..., gt=0, description="Height scale (metres)")
    z: float = Field(..., gt=0, description="Depth scale (metres)")


class ARModelMetadata(BaseModel):
    """
    Full AR asset descriptor returned by GET /assets/ar-model/{species_id}.
    Consumed by the Flutter ARCore / SceneView widget.
    """
    species_id: str = Field(..., examples=["azadirachta_indica"])
    common_name: str = Field(..., examples=["Neem"])
    scientific_name: str = Field(..., examples=["Azadirachta indica"])
    kannada_name: str | None = Field(None, examples=["ಬೇವು"])

    # ── 3D asset references ──────────────────────────────────────────────────
    # glTF 2.0 is the preferred format for ARCore/SceneView
    gltf_url: str = Field(..., description="Public GCS URL of the .glb (binary glTF) model")
    thumbnail_url: str = Field(..., description="Preview image for the species card")

    # ── AR placement parameters ──────────────────────────────────────────────
    real_world_scale_m: ARModelScale = Field(
        ..., description="Bounding box in metres at adult size"
    )
    # Sapling scale is shown immediately on adoption; adult after 5 years
    sapling_scale_factor: float = Field(
        default=0.15,
        description="Multiply adult scale by this to render a sapling",
    )
    # Y-axis offset so the trunk base sits on the AR ground plane
    ground_offset_m: float = Field(default=0.0, ge=0)

    # ── Environmental overlay data (shown as AR info cards) ──────────────────
    co2_absorption_kg_per_year: float
    expected_canopy_spread_m: float
    water_requirement: str
    growth_rate: str

    # ── Provenance ───────────────────────────────────────────────────────────
    model_version: str = "1.0"
    last_updated: datetime = Field(default_factory=datetime.utcnow)


