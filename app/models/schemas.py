# FEATURE 2 — Green Corridor Clustering
# ══════════════════════════════════════════════════════════════════════════════

class CorridorStatus(str, Enum):
    ACTIVE = "active"           # 5+ verified trees within 100m radius
    EMERGING = "emerging"       # 3–4 verified trees — almost there
    INACTIVE = "inactive"       # Fewer than 3 verified trees


class GreenCorridor(BaseModel):
    """
    A geospatial cluster of verified planted trees constituting
    an Active Green Corridor in a BBMP ward.
    """
    corridor_id: str
    ward_name: str
    ward_id: str
    centre_coordinates: Coordinates
    radius_m: float = 100.0
    verified_tree_count: int
    status: CorridorStatus
    # User IDs of all contributors within this corridor
    contributor_user_ids: list[str] = []
    # Badge awarded to contributors of active corridors
    badge_awarded: str | None = None
    detected_at: datetime = Field(default_factory=datetime.utcnow)
    last_audited: datetime = Field(default_factory=datetime.utcnow)


class CorridorAuditResult(BaseModel):
    """Result returned by POST /corridors/audit"""
    wards_audited: int
    new_corridors_detected: int
    badges_awarded: int
    corridors: list[GreenCorridor]
    audit_duration_seconds: float


