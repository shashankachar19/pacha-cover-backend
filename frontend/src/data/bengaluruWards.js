// Deterministic risk assignment based on ward coordinates
// (mirrors backend mock logic for visual consistency)
export function getWardRisk(ward) {
  const seed = ward.lat * 1000 + ward.lng;
  const h = Math.sin(seed) * 43758.5453;
  const score = ((h - Math.floor(h)) * 100);
  // High-density central areas tend to be hotter
  const centralPenalty = (Math.abs(ward.lat - 12.97) + Math.abs(ward.lng - 77.59)) < 0.05 ? 15 : 0;
  const finalScore = Math.min(100, score + centralPenalty);
  let level;
  if (finalScore > 75) level = 'critical';
  else if (finalScore > 55) level = 'high';
  else if (finalScore > 35) level = 'moderate';
  else level = 'low';
  return { score: Math.round(finalScore), level, ndvi: +(0.1 + (1 - finalScore/100) * 0.4).toFixed(3), lst: +(28 + (finalScore/100) * 12).toFixed(1) };
}
